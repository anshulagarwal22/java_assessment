package StringMethods;

public class Replace {
	   public static void main(String args[]) {
	         
		      
		      String Str = new String("Hello This is anshul ");
		      System.out.println(Str);
		      
		      // Using replace to replace characters
		      
		      System.out.println("After replacing all a with A : " );
		      System.out.println(Str.replace('a', 'A'));
		      System.out.println("After replacing all T with t : " );
		      System.out.println(Str.replace('T', 't'));
		      
		  
		        
		   }
		}